"""NCC-based SCAN RESULTS title anchor for the SC mining HUD.

Replaces the Tesseract-based ``_find_scan_results_anchor`` in
``ocr/onnx_hud_reader.py``. The "SCAN RESULTS" title is the most
stable feature of the rock-scan panel: large bold static text, always
at the top, identical pixel rendering across rocks/lighting/HUD
colors. NCC against a canonicalized template gives a tilt-tolerant,
polarity-independent anchor in ~5 ms per scan — vs Tesseract's
50-200 ms with bbox inflation on tilted text.

Templates live at ``ocr/sc_templates/scan_results.npz`` and are
generated by ``scripts/build_scan_results_template.py`` from
annotated panel captures (``.boxes.json`` sidecars containing
``scan_results: {x, y, w, h}``).

Public API mirrors the dict shape of the prior Tesseract anchor so
``_label_rows_from_anchor`` can consume either::

    from ocr.sc_ocr.scan_results_match import find_scan_results_anchor

    anchor = find_scan_results_anchor(img)
    # anchor = {
    #     "title_x": int,    # left edge of "SCAN RESULTS" match
    #     "title_y": int,    # top of match
    #     "title_w": int,    # width at the matched scale
    #     "title_h": int,    # height at the matched scale
    #     "score":   float,  # NCC score at the matched scale
    # }
    # or None if no match crosses the confidence threshold.
"""
from __future__ import annotations

import logging
import os
import threading
import time
from typing import Optional

import numpy as np
from PIL import Image

from . import label_match as _lm

log = logging.getLogger(__name__)

# Templates path (next to the label templates).
_THIS_DIR = os.path.dirname(os.path.abspath(__file__))
_TOOL_DIR = os.path.normpath(os.path.join(_THIS_DIR, "..", ".."))
_TEMPLATES_PATH = os.path.join(
    _TOOL_DIR, "ocr", "sc_templates", "scan_results.npz",
)

# Cached normalized template across scales.
# Each entry: (template_float32_zero_mean_unit_var, w, h)
_TEMPLATE_CACHE: Optional[list[tuple[np.ndarray, int, int]]] = None
_TEMPLATE_NATIVE_HEIGHT: int = 28

# Acceptable NCC score for a confident SCAN RESULTS match. Below this,
# the anchor is considered "not found" and the caller falls back to
# the Tesseract anchor / position-based finder.
#
# Calibrated against 120 real SC captures from
# training_data_panels/user_20260418_154408/region1: 99.2% match at
# 0.40, 98.3% at 0.50, 95.0% at 0.60. The misses at 0.50 are panels
# with heavy particle/snow occlusion of the title (e.g. mining
# through ice debris) — those fail Tesseract too, so 0.40 is the
# sweet spot for capturing the recoverable cases.
_MIN_MATCH_SCORE = 0.40

# Multi-scale search. The captured panel can be at different scales
# (different SC HUD render sizes, different upstream upscaling, etc.),
# so we search across a range and keep the best score.
_SCALE_FACTORS = (0.6, 0.75, 0.9, 1.0, 1.15, 1.35, 1.6, 2.0)

# Restrict search to the top portion of the captured image. SCAN RESULTS
# is always at the top of the panel — searching the whole image risks
# matching against COMPOSITION rows or commodity text below.
#
# Tightened from 0.55 → 0.42 because on dark backgrounds the matcher
# was correlating against the bright digits in the MASS row (the title
# template at smaller scales correlates with "20355"-style strings due
# to similar vertical-stroke patterns). Title is always within the top
# ~35% of any reasonable capture region; 0.42 gives ~7% headroom for
# weird HUD scales without letting value-row digits compete for the
# anchor position.
_SEARCH_TOP_FRACTION = 0.42

# Per-image cache: avoid running the multi-scale sweep twice when
# called from multiple paths in a single scan.
#
# Cache key shape:
#   (id(img), size, mode, search_center, search_radius)
# where ``search_center`` is None for full-frame calls and a
# (x, y) tuple for local-search calls. Including the search
# parameters in the key prevents a local-search call from
# returning a stale full-frame result (and vice-versa) when both
# happen against the same Image instance in a single scan.
_LAST_CALL_CACHE: Optional[tuple[
    int,                          # id(img)
    tuple[int, int],              # img.size
    str,                          # img.mode
    Optional[tuple[int, int]],    # search_center
    int,                          # search_radius
    Optional[dict],               # result
]] = None


# ─── Cross-frame anchor tracker (anti-jitter) ─────────────────────────────
#
# The raw NCC anchor is sub-pixel-noisy frame to frame: even when the HUD
# is stationary, successive detections land at slightly different (x, y)
# because of bilinear-resize quantization in the multi-scale sweep and
# small NCC peak shifts when scene lighting shifts a few pixels of
# foreground/background. Without smoothing, the cyan row bands in the
# live overlay jump around the screen and downstream label-row matchers
# spend their hysteresis budget re-locking when the anchor drifted only
# a few pixels.
#
# In production we ALSO see occasional catastrophic mis-detections — the
# NCC sweep correlates strongly against value-row digits in some
# captures, returning an anchor 15-25 px off the true title position.
# Those produce wildly-wrong rows (e.g. resistance "1%" landing on the
# instability row). A small outlier-hysteresis layer rejects those for
# 1 frame, gives them through if they persist (real panel jump),
# preventing single-frame mis-detections from corrupting the lock cache.
#
# Strategy:
#   * Distance ≤ _TRACK_JITTER_PX (10 px): EMA blend α=0.5 — absorbs
#     frame-to-frame noise without significant latency. Track resets
#     immediately to the new value if it's a real shift.
#   * Distance ≤ _TRACK_MOTION_PX (25 px): EMA blend α=0.35 — could be
#     real motion (player turning view) or a slightly off detection;
#     bias mildly toward stability.
#   * Distance > _TRACK_MOTION_PX: HYSTERESIS. Reject for the first
#     frame, accept on the second consecutive large jump (treated as
#     real panel motion). This kills the catastrophic single-frame
#     mis-detection failure mode while still tracking honest pans.
#   * Tracker history older than _TRACK_FRESH_SEC (3 s): treat as
#     stale — accept the raw detection unconditionally so a returning
#     player isn't stuck behind a 3-second-old position.
#
# Thread-safe: multiple scan workers may call ``find_scan_results_anchor``
# concurrently (api.py uses a thread pool). All tracker reads/writes
# happen under ``_anchor_tracker_lock``.
_TRACK_JITTER_PX = 10.0
_TRACK_MOTION_PX = 25.0
# Tracker freshness window. Tuned to span the outer ``_find_label_rows``
# anchor cache (5s in onnx_hud_reader.py) plus headroom so the tracker
# state survives across at least one cache-expiry boundary, which is
# where most jitter is observed (the cache otherwise hides
# frame-to-frame variation by freezing the position for 5 s, then a
# fresh detection lands 10-25 px elsewhere on cache miss). Lower values
# (e.g. 3 s) caused the tracker to reset baseline at every boundary,
# disabling smoothing exactly when it was needed.
_TRACK_FRESH_SEC = 10.0
_TRACK_OUTLIER_HYSTERESIS = 2   # frames to confirm a real large jump

_anchor_tracker_lock = threading.Lock()
_anchor_tracker_state: dict = {
    "smoothed": None,         # last accepted anchor dict, or None
    "ts": 0.0,                # monotonic time of the last update
    "outlier_streak": 0,      # consecutive frames in the large-jump band
}

# ── Baseline-reset callback hook ──────────────────────────────────────
# The lock cache in api.py is populated from the crop pixels at the
# tracker's CURRENT smoothed position. When the tracker's outlier
# hysteresis confirms a sustained large jump and re-baselines to a new
# position, those previously-built locks reference the OLD anchor's
# pixel region and are no longer trustworthy. Downstream subscribers
# (api.py) register a callback to flush their lock state on this event.
#
# The callback list is intentionally process-global and append-only.
# Workers that fire callbacks read a snapshot (``list(...)``) so a
# late registration during a fire is safe. Registration itself is
# expected to happen at import time only — there is no removal API
# outside of the test helper, and we don't lock here because writes
# are one-shot at module init.
_baseline_reset_callbacks: list = []


def register_baseline_reset_callback(fn) -> None:
    """Register a function called when the tracker re-baselines after
    a sustained outlier streak. The callback fires AFTER the jump is
    confirmed (>= ``_TRACK_OUTLIER_HYSTERESIS`` consecutive outliers).

    Used by the lock cache to invalidate downstream state — if the
    anchor moved, locks built on the previous anchor's crop pixels
    are no longer trustworthy.

    Best-effort: callbacks that raise are logged + skipped. The
    callback runs INSIDE the tracker lock but should be cheap;
    long-running work should be deferred to the caller.
    """
    _baseline_reset_callbacks.append(fn)


def _clear_baseline_reset_callbacks() -> None:
    """Test-only: drop all registered callbacks.

    Production code never calls this. Tests use it to start with a
    clean slate so they can register their own listener and observe
    just its firing behaviour.
    """
    _baseline_reset_callbacks.clear()


def _fire_baseline_reset_callbacks() -> None:
    """Invoke every registered callback in registration order.

    Each callback is called inside try/except so one bad listener
    doesn't stop the others from being notified.
    """
    for cb in list(_baseline_reset_callbacks):
        try:
            cb()
        except Exception as exc:
            log.warning("baseline_reset_callback failed: %s", exc)


def _smooth_anchor(raw: Optional[dict], now: float) -> Optional[dict]:
    """Blend a fresh raw anchor with the cross-frame tracker state.

    Returns the smoothed anchor that callers should use. Updates the
    tracker state as a side effect. Pure-Python, no NumPy — runs in
    microseconds. See module-scope comment for the full state machine.

    Side effect: when the outlier-hysteresis branch confirms a real
    panel jump, fires every registered baseline-reset callback AFTER
    releasing the tracker lock — so listeners that themselves call
    back into ``scan_results_match`` cannot deadlock.
    """
    if raw is None:
        return None
    # Set to True inside the lock when the outlier-hysteresis branch
    # confirms a real panel jump. Firing happens after the lock is
    # released, below.
    fire_baseline_reset = False
    result: Optional[dict] = None
    with _anchor_tracker_lock:
        prev = _anchor_tracker_state["smoothed"]
        prev_ts = float(_anchor_tracker_state["ts"] or 0.0)
        age = now - prev_ts
        if prev is None or age > _TRACK_FRESH_SEC:
            # No prior or stale prior — accept raw as new baseline.
            out = dict(raw)
            _anchor_tracker_state["smoothed"] = out
            _anchor_tracker_state["ts"] = now
            _anchor_tracker_state["outlier_streak"] = 0
            log.debug(
                "scan_results_match: tracker baseline reset (age=%.1fs) "
                "x=%d y=%d", age, out["title_x"], out["title_y"],
            )
            result = out
        else:
            dx = float(raw["title_x"]) - float(prev["title_x"])
            dy = float(raw["title_y"]) - float(prev["title_y"])
            dist = (dx * dx + dy * dy) ** 0.5
            if dist <= _TRACK_JITTER_PX:
                alpha = 0.5
                _anchor_tracker_state["outlier_streak"] = 0
                ema_blend = True
            elif dist <= _TRACK_MOTION_PX:
                alpha = 0.35
                _anchor_tracker_state["outlier_streak"] = 0
                ema_blend = True
            else:
                # Big jump — either real motion or a single-frame
                # mis-detection. Apply hysteresis: reject for the
                # first frame, accept on the second consecutive jump.
                #
                # EXCEPT for the "strong dominant match" override: if
                # the raw candidate has both a much larger template
                # size AND a much higher score than the smoothed
                # reference, snap to it immediately. This recovers
                # from the failure mode where the smoother got locked
                # onto a degenerate small-scale false positive (e.g. a
                # 95x17 NCC match against a different glyph at small
                # scale, score 0.65) while the real title (253x45,
                # score 1.0+) is being rejected every frame as an
                # outlier. Without this override the smoother stays
                # wrong until reset_anchor_tracker() is called
                # externally, which the user has no UI to trigger.
                #
                # Thresholds:
                #   raw_w >= 2 * prev_w  — the canonical title is much
                #     larger than a small-scale glyph match; a real
                #     panel jump wouldn't change the title size by 2x
                #     within one frame.
                #   raw_score >= prev_score + 0.3 — substantially
                #     stronger correlation, indicating the new match
                #     is the "real" anchor.
                raw_w = int(raw.get("title_w", 0))
                prev_w = max(1, int(prev.get("title_w", 1)))
                raw_score = float(raw.get("score", 0.0))
                prev_score = float(prev.get("score", 0.0))
                strong_dominant = (
                    raw_w >= 2 * prev_w
                    and raw_score >= prev_score + 0.3
                )
                if strong_dominant:
                    out = dict(raw)
                    _anchor_tracker_state["smoothed"] = out
                    _anchor_tracker_state["ts"] = now
                    _anchor_tracker_state["outlier_streak"] = 0
                    log.warning(
                        "scan_results_match: tracker OVERRIDE — "
                        "strong dominant match (w=%d score=%.2f) replaces "
                        "weak smoothed reference (w=%d score=%.2f) at "
                        "dist=%.1fpx; snapping to new pose (%d,%d)",
                        raw_w, raw_score, prev_w, prev_score, dist,
                        out["title_x"], out["title_y"],
                    )
                    result = out
                    fire_baseline_reset = True
                    ema_blend = False
                else:
                    ema_blend = False
                    streak = int(
                        _anchor_tracker_state.get("outlier_streak") or 0
                    ) + 1
                    _anchor_tracker_state["outlier_streak"] = streak
                    if streak < _TRACK_OUTLIER_HYSTERESIS:
                        log.info(
                            "scan_results_match: tracker REJECTED outlier "
                            "(dist=%.1fpx streak=%d/%d) raw=(%d,%d) using "
                            "smoothed=(%d,%d)", dist, streak,
                            _TRACK_OUTLIER_HYSTERESIS,
                            raw["title_x"], raw["title_y"],
                            prev["title_x"], prev["title_y"],
                        )
                        # Don't update ts — the next fresh frame still
                        # sees the old age. We want successive outliers
                        # to accumulate toward the hysteresis threshold
                        # quickly.
                        result = dict(prev)
                    else:
                        # Confirmed real motion. Snap to the raw value.
                        out = dict(raw)
                        _anchor_tracker_state["smoothed"] = out
                        _anchor_tracker_state["ts"] = now
                        _anchor_tracker_state["outlier_streak"] = 0
                        log.info(
                            "scan_results_match: tracker ACCEPTED panel jump "
                            "(dist=%.1fpx after %d outlier frames) x=%d y=%d",
                            dist, streak, out["title_x"], out["title_y"],
                        )
                        result = out
                        # Defer callback fire until after the tracker
                        # lock is released, so a callback that re-enters
                        # this module (e.g. reset_anchor_tracker) cannot
                        # deadlock.
                        fire_baseline_reset = True
            if ema_blend:
                # EMA blend.
                out = dict(raw)
                for k in ("title_x", "title_y", "title_w", "title_h"):
                    out[k] = int(round(
                        alpha * float(raw[k]) + (1.0 - alpha) * float(prev[k])
                    ))
                # Score is always the freshly-measured one — smoothing
                # positions but not the quality signal.
                out["score"] = float(
                    raw.get("score", prev.get("score", 0.0))
                )
                _anchor_tracker_state["smoothed"] = out
                _anchor_tracker_state["ts"] = now
                result = out
    # Lock released. Notify subscribers that the baseline moved.
    # The lock cache in api.py must invalidate because its stored
    # crop fingerprints reference the OLD anchor's pixels. Best-
    # effort: misbehaving callbacks are caught + logged so the
    # tracker itself stays robust.
    if fire_baseline_reset:
        _fire_baseline_reset_callbacks()
    return result


def reset_anchor_tracker() -> None:
    """Clear the cross-frame tracker state. Call when the panel has
    definitively disappeared (rock destroyed, player left scanning
    mode) so the next detection starts fresh without smoothing against
    a stale position from a previous rock.
    """
    with _anchor_tracker_lock:
        _anchor_tracker_state["smoothed"] = None
        _anchor_tracker_state["ts"] = 0.0
        _anchor_tracker_state["outlier_streak"] = 0


def _load_template() -> Optional[list[tuple[np.ndarray, int, int]]]:
    """Load the SCAN RESULTS template from disk and pre-build scale variants.

    Returns a list of (canonical_template, w, h) at every scale in
    ``_SCALE_FACTORS``, or None if the npz file is missing.
    Cached after first load.
    """
    global _TEMPLATE_CACHE, _TEMPLATE_NATIVE_HEIGHT
    if _TEMPLATE_CACHE is not None:
        return _TEMPLATE_CACHE
    if not os.path.isfile(_TEMPLATES_PATH):
        log.warning(
            "scan_results_match: template not found at %s — run "
            "scripts/build_scan_results_template.py to bootstrap it",
            _TEMPLATES_PATH,
        )
        return None
    try:
        data = np.load(_TEMPLATES_PATH)
        if "scan_results" not in data:
            log.warning(
                "scan_results_match: 'scan_results' key missing in %s",
                _TEMPLATES_PATH,
            )
            return None
        raw = data["scan_results"].astype(np.float32)
        if "height" in data:
            _TEMPLATE_NATIVE_HEIGHT = int(data["height"])
        # Normalize once at native scale: zero-mean, unit-variance.
        mean = float(raw.mean())
        std = float(raw.std())
        if std < 1e-3:
            log.warning("scan_results_match: template degenerate (std≈0)")
            return None
        native = (raw - mean) / std
        # Pre-build scale variants. Each variant is re-normalized after
        # resampling because PIL's bilinear resize shifts the mean/std
        # slightly — needed so NCC's normalization invariant holds.
        variants: list[tuple[np.ndarray, int, int]] = []
        for s in _SCALE_FACTORS:
            try:
                resized = _lm._resize_template(native, s)
                if resized.shape[0] >= 8 and resized.shape[1] >= 8:
                    variants.append(
                        (resized, resized.shape[1], resized.shape[0])
                    )
            except Exception:
                continue
        if not variants:
            log.warning(
                "scan_results_match: no usable scale variants built",
            )
            return None
        _TEMPLATE_CACHE = variants
        log.info(
            "scan_results_match: loaded template (native h=%d) → %d "
            "scale variants from %s",
            _TEMPLATE_NATIVE_HEIGHT, len(variants), _TEMPLATES_PATH,
        )
        return _TEMPLATE_CACHE
    except Exception as exc:
        log.warning("scan_results_match: template load failed: %s", exc)
        return None


def reset_cache() -> None:
    """Force the template + per-image cache to rebuild on next call.

    Call after rebuilding ``scan_results.npz`` so the new template
    takes effect without restarting the process.
    """
    global _TEMPLATE_CACHE, _LAST_CALL_CACHE
    _TEMPLATE_CACHE = None
    _LAST_CALL_CACHE = None


def find_scan_results_anchor(
    img: Image.Image,
    *,
    search_center: Optional[tuple[int, int]] = None,
    search_radius: int = 60,
) -> Optional[dict]:
    """Locate the SCAN RESULTS title in ``img`` via multi-scale NCC.

    Searches the top ``_SEARCH_TOP_FRACTION`` of the image (top 55% by
    default) at every scale in ``_SCALE_FACTORS`` and returns the
    BEST-scoring position across all scales — provided that score
    crosses ``_MIN_MATCH_SCORE``.

    Returns ``{"title_x", "title_y", "title_w", "title_h", "score"}``
    matching the dict shape ``_label_rows_from_anchor`` expects, or
    ``None`` if no scale produced a confident match. Coordinates are
    always in IMAGE-ABSOLUTE pixels, regardless of whether a local
    search window was used.

    Local search mode
    -----------------
    When ``search_center`` is provided, the NCC search is restricted
    to a window ``[cx - search_radius, cx + search_radius] ×
    [cy - search_radius, cy + search_radius]`` (clamped to image
    bounds). This is how the rigid-body HUD tracker asks the matcher
    to "check near where the title was last frame" instead of paying
    for a full-frame sweep every frame.

    Local search semantics differ from full-frame in two ways:
      * The cross-frame EMA smoothing tracker is BYPASSED — the
        tracker owns its own state machine in that mode, and double-
        smoothing would lag panel motion. Full-frame callers still
        get smoothing for backwards compatibility.
      * A miss returns ``None`` immediately — the caller decides
        whether to escalate to a full-frame search.

    Per-image cache: a single-entry "last image" cache keyed on
    ``(id(img), img.size, img.mode, search_center, search_radius)``
    returns the cached result when the same image is passed multiple
    times with the same search arguments. Critical because
    ``_find_label_rows`` and other entry points may both call this in
    a single scan. Local and full-frame requests for the same image
    have DIFFERENT cache entries so they can't return stale data to
    each other.
    """
    global _LAST_CALL_CACHE
    # Normalise the search_center to a hashable tuple-of-int (or None)
    # so caller-side `[x, y]` lists and numpy ints still produce the
    # same cache key as `(x, y)` python tuples.
    if search_center is not None:
        sc_norm: Optional[tuple[int, int]] = (
            int(search_center[0]), int(search_center[1]),
        )
    else:
        sc_norm = None
    sr_norm = int(search_radius)
    cache_key = (id(img), img.size, img.mode, sc_norm, sr_norm)
    if _LAST_CALL_CACHE is not None and _LAST_CALL_CACHE[:5] == cache_key:
        return _LAST_CALL_CACHE[5]

    variants = _load_template()
    if not variants:
        _LAST_CALL_CACHE = (*cache_key, None)
        return None

    W, H = img.size
    if W < 40 or H < 40:
        _LAST_CALL_CACHE = (*cache_key, None)
        return None

    # ── Determine the search region ──
    # full-frame mode: top _SEARCH_TOP_FRACTION of the image, full width.
    # local mode: ±search_radius around search_center, plus enough
    # template-sized slack on every side so the WHOLE template still
    # fits when its center lands at the edge of the requested
    # tolerance band. Clamped to image bounds. We keep an
    # (off_x, off_y) "where this crop sits in image coordinates" so
    # the final reported (title_x, title_y) is always image-absolute
    # regardless of which mode we used.
    if sc_norm is None:
        # Restrict to top portion. The title is always near the top of
        # the panel — searching the bottom risks matching against
        # COMPOSITION / outcome bars / commodity rows.
        search_h = max(40, int(H * _SEARCH_TOP_FRACTION))
        off_x, off_y = 0, 0
        crop = img.crop((0, 0, W, search_h)).convert("RGB")
    else:
        cx, cy = sc_norm
        r = max(1, sr_norm)
        # Pad by the LARGEST template dimension across all scales so
        # that any title whose CENTER lands within ±r of (cx, cy)
        # has its full bounding box inside the crop. Without this
        # pad, the search window for a ±60 px tolerance would be
        # only 120 px wide — narrower than the SCAN RESULTS template
        # at its largest scale (~316 px wide) — and the largest
        # scales would silently get skipped, producing False
        # negatives for callers that are looking IN the right place.
        max_tw = max(t[1] for t in variants)
        max_th = max(t[2] for t in variants)
        pad_x = max_tw // 2 + 4
        pad_y = max_th // 2 + 4
        x1 = max(0, cx - r - pad_x)
        y1 = max(0, cy - r - pad_y)
        x2 = min(W, cx + r + pad_x)
        y2 = min(H, cy + r + pad_y)
        if x2 - x1 < 8 or y2 - y1 < 8:
            # Window degenerate (caller passed a center fully off-screen).
            _LAST_CALL_CACHE = (*cache_key, None)
            return None
        off_x, off_y = x1, y1
        crop = img.crop((x1, y1, x2, y2)).convert("RGB")
    rgb = np.array(crop, dtype=np.uint8)
    # Use max-of-RGB-channels rather than ITU-R 601 luma. The SC HUD
    # title renders with chromatic aberration on dark backgrounds
    # (cyan/red color fringes around each glyph). Luma blends the
    # channels with weights (R*0.30, G*0.59, B*0.11), which spatially
    # blurs the AA'd strokes and tanks NCC against a clean template.
    # Max-channel keeps each glyph's brightest channel intact, so the
    # stroke shape NCC was trained on stays recognizable.
    detect = rgb.max(axis=2).astype(np.uint8)
    target = _lm._canonicalize(detect)

    # Two-stage ranking strategy:
    #   1. Per scale, get the global NCC max + position from _ncc_search.
    #   2. Discard scales whose raw NCC didn't clear _MIN_MATCH_SCORE —
    #      they're noise candidates and shouldn't compete for the top slot.
    #   3. Among surviving candidates, rank by (raw_score + top_bias).
    #      A strong top-bias means a candidate near y=0 wins over a
    #      candidate near the bottom of the search region even if the
    #      latter has a slightly higher raw score. This is correct for
    #      SCAN RESULTS specifically: the title is structurally always
    #      at the top, so a competing match deeper in the panel (e.g.
    #      against bright value-row digits) is almost certainly wrong.
    #   4. Threshold gate uses the raw score of the chosen candidate,
    #      so we never accept a "biased high but real-low" match.
    #
    # Bias of 0.30 was sized against real captures: the worst observed
    # gap between an actual title's NCC and a wrong-region NCC was ~0.10,
    # and the actual title typically sits at y≤30% of search_h vs the
    # wrong region at y≥80%. Bias delta of 0.30*(0.80-0.30)=0.15 cleanly
    # overrides the 0.10 score gap.
    _Y_BIAS_MAX = 0.30
    target_h = max(1, target.shape[0])

    candidates: list[tuple[float, int, int, int, int]] = []  # (raw, x, y, w, h)
    for tmpl, tw, th in variants:
        if tw > target.shape[1] or th > target.shape[0]:
            continue
        score, x, y = _lm._ncc_search(target, tmpl)
        if score < _MIN_MATCH_SCORE:
            continue
        candidates.append((float(score), int(x), int(y), int(tw), int(th)))

    # Local-search filter: a candidate is only valid if its CENTER
    # (in image-absolute coordinates) lies within ``search_radius``
    # of the requested ``search_center``. We pad the crop generously
    # so NCC has enough context to land cleanly on glyphs at any
    # scale, but the contract is "tell me about titles near here" —
    # a title that happens to fall inside the padded region but
    # outside the ±r tolerance should NOT be reported.
    if sc_norm is not None:
        cx, cy = sc_norm
        r = max(1, sr_norm)
        r_sq = r * r
        filtered: list[tuple[float, int, int, int, int]] = []
        for raw, x, y, tw, th in candidates:
            # Candidate center in image coordinates.
            abs_cx = x + off_x + tw // 2
            abs_cy = y + off_y + th // 2
            dx = abs_cx - cx
            dy = abs_cy - cy
            # Use Chebyshev (rectangular) tolerance — matches the
            # rectangular ±r window semantics in the docstring.
            if abs(dx) <= r and abs(dy) <= r:
                filtered.append((raw, x, y, tw, th))
        if not filtered:
            log.debug(
                "scan_results_match: local search produced %d raw "
                "candidates but none within ±%d of (%d, %d)",
                len(candidates), r, cx, cy,
            )
        candidates = filtered

    if not candidates:
        log.debug(
            "scan_results_match: no scale cleared threshold=%.2f",
            _MIN_MATCH_SCORE,
        )
        _LAST_CALL_CACHE = (*cache_key, None)
        return None

    # Pick the candidate with the highest BIASED rank. Top-bias dominates
    # tight raw-score competitions; raw threshold has already filtered
    # noise out, so we know every candidate is at least plausible.
    #
    # NOTE: in local-search mode the y-bias is essentially neutralised
    # because every candidate sits inside a ±search_radius window — the
    # tracker has already pinned the expected y, so we don't want to
    # over-prefer the top of the window. We keep the bias formula
    # uniform anyway because full-frame regression is what matters and
    # the tiny local-mode effect is within noise.
    def _biased(c):
        raw, _x, y, _w, _h = c
        return raw + _Y_BIAS_MAX * (1.0 - (y / target_h))

    candidates.sort(key=_biased, reverse=True)
    best_score, _bx, _by, _bw, _bh = candidates[0]
    best_pos = (_bx, _by, _bw, _bh)

    # Diagnostic: if the topmost-by-bias differs from the topmost-by-raw,
    # log both so we can see when the bias is actually doing work.
    if len(candidates) > 1:
        raw_winner = max(candidates, key=lambda c: c[0])
        if raw_winner is not candidates[0]:
            log.debug(
                "scan_results_match: bias chose y=%d raw=%.3f over "
                "raw-winner y=%d raw=%.3f (likely false-match suppression)",
                candidates[0][2], candidates[0][0],
                raw_winner[2], raw_winner[0],
            )

    x, y, w, h = best_pos
    # Convert window-local match position back to image-absolute
    # coordinates. In full-frame mode off_x/off_y are (0, 0), so this
    # is a no-op for the legacy code path.
    abs_x = int(x) + int(off_x)
    abs_y = int(y) + int(off_y)
    raw_anchor = {
        "title_x": abs_x,
        "title_y": abs_y,
        "title_w": int(w),
        "title_h": int(h),
        "score":   float(best_score),
    }
    log.debug(
        "scan_results_match: matched at x=%d y=%d w=%d h=%d score=%.3f "
        "(mode=%s)",
        abs_x, abs_y, w, h, best_score,
        "local" if sc_norm is not None else "full",
    )
    if sc_norm is None:
        # Run through the cross-frame anti-jitter tracker. Absorbs sub-pixel
        # NCC noise, smooths frame-to-frame jitter, rejects single-frame
        # outliers via hysteresis. See _smooth_anchor docstring.
        #
        # Local-search callers (the rigid-body HUD tracker) bypass this
        # path: they manage their own smoothing / hysteresis at the
        # tracker layer, and double-filtering would lag panel motion.
        anchor = _smooth_anchor(raw_anchor, time.monotonic())
        if anchor is not None and (
            anchor["title_x"] != raw_anchor["title_x"]
            or anchor["title_y"] != raw_anchor["title_y"]
        ):
            log.debug(
                "scan_results_match: tracker smoothed (%d,%d) -> (%d,%d)",
                raw_anchor["title_x"], raw_anchor["title_y"],
                anchor["title_x"], anchor["title_y"],
            )
    else:
        # Local search: return the raw measurement as-is. Caller owns
        # smoothing.
        anchor = raw_anchor
    _LAST_CALL_CACHE = (*cache_key, anchor)
    return anchor
